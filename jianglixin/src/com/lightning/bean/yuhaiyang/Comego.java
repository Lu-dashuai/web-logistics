package com.lightning.bean.yuhaiyang;


public class Comego {
	private Integer comego_id;//出库ID
	private Integer comego_indent;//订单编号
	private String comego_time;//出库时间
	private Integer comego_station;//网点ID（所属网点）
	private int comego_car;//车辆ID
	
	public Comego() {
		super();
	}
	public Comego(Integer comego_id, Integer comego_indent, String comego_time, Integer comego_station,
			int comego_car) {
		super();
		this.comego_id = comego_id;
		this.comego_indent = comego_indent;
		this.comego_time = comego_time;
		this.comego_station = comego_station;
		this.comego_car = comego_car;
	}
	public Integer getComego_id() {
		return comego_id;
	}
	public void setComego_id(Integer comego_id) {
		this.comego_id = comego_id;
	}
	public Integer getComego_indent() {
		return comego_indent;
	}
	public void setComego_indent(Integer comego_indent) {
		this.comego_indent = comego_indent;
	}
	public String getComego_time() {
		return comego_time;
	}
	public void setComego_time(String comego_time) {
		this.comego_time = comego_time;
	}
	public Integer getComego_station() {
		return comego_station;
	}
	public void setComego_station(Integer comego_station) {
		this.comego_station = comego_station;
	}
	public int getComego_car() {
		return comego_car;
	}
	public void setComego_car(int comego_car) {
		this.comego_car = comego_car;
	}
	
	
}
