package com.lightning.bean.yuhaiyang;

public class Time {
	private int time_id;//编号
	private int time_car_id;//订单所属车辆     关联车辆表
	private String time_start;//发车时间
	private String time_end;//到达时间
	private int time_date;//预计运输天数
	public Time() {
		super();
	}
	
	public Time(int time_car_id, String time_start, String time_end, int time_date) {
		super();
		this.time_car_id = time_car_id;
		this.time_start = time_start;
		this.time_end = time_end;
		this.time_date = time_date;
	}
	public int getTime_id() {
		return time_id;
	}
	public void setTime_id(int time_id) {
		this.time_id = time_id;
	}
	public int getTime_car_id() {
		return time_car_id;
	}
	public void setTime_car_id(int time_car_id) {
		this.time_car_id = time_car_id;
	}
	public String getTime_start() {
		return time_start;
	}
	public void setTime_start(String time_start) {
		this.time_start = time_start;
	}
	public String getTime_end() {
		return time_end;
	}
	public void setTime_end(String time_end) {
		this.time_end = time_end;
	}
	public int getTime_date() {
		return time_date;
	}
	public void setTime_date(int time_date) {
		this.time_date = time_date;
	}
	
}
