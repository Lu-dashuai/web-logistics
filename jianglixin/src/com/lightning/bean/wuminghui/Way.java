package com.lightning.bean.wuminghui;

public class Way {
	private int way_car;//车辆id 不是null 主键
	private int way_direction;//运送哪里的网点
	private float way_km;//运输距离
	public int getWay_car() {
		return way_car;
	}
	public void setWay_car(int way_car) {
		this.way_car = way_car;
	}
	public int getWay_direction() {
		return way_direction;
	}
	public void setWay_direction(int way_direction) {
		this.way_direction = way_direction;
	}
	public float getWay_km() {
		return way_km;
	}
	public void setWay_km(float way_km) {
		this.way_km = way_km;
	}
	public Way(int way_car, int way_direction, float way_km) {
		super();
		this.way_car = way_car;
		this.way_direction = way_direction;
		this.way_km = way_km;
	}
	public Way() {
		super();
		// TODO Auto-generated constructor stub
	}

	
}
